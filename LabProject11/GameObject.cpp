{
}

void CGameObject::Render(ID3D12GraphicsCommandList* pd3dCommandList, CCamera* pCamera)
{
	OnPrepareRender();

	UpdateShaderVariables(pd3dCommandList);

	if (m_pShader) {
		//게임 객체의 월드 변환 행렬을 셰이더의 상수 버퍼로 전달(복사)한다. 
		//m_pShader->UpdateShaderVariable(pd3dCommandList, &m_xmf4x4World);
		m_pShader->Render(pd3dCommandList, pCamera);
	}

	if (m_pMesh) m_pMesh->Render(pd3dCommandList);
}

void CGameObject::CreateShaderVariables(ID3D12Device* pd3dDevice, ID3D12GraphicsCommandList* pd3dCommandList)
{
	// 월드 변환 행렬을 위한 리소스 생성(CBV)
	D3D12_HEAP_PROPERTIES d3dHeapProperties;
	::ZeroMemory(&d3dHeapProperties, sizeof(D3D12_HEAP_PROPERTIES));
	d3dHeapProperties.Type = D3D12_HEAP_TYPE_UPLOAD; // 업로드 힙
	D3D12_RESOURCE_DESC d3dResourceDesc;
	d3dResourceDesc.Dimension = D3D12_RESOURCE_DIMENSION_BUFFER; // 버퍼
	d3dResourceDesc.Alignment = 0; // 메모리 정렬
	d3dResourceDesc.Width = (sizeof(XMFLOAT4X4) + 255) & ~255; // 상수 버퍼의 크기는 256의 배수
	d3dResourceDesc.Height = 1; // 배열의 크기
	d3dResourceDesc.DepthOrArraySize = 1; // 배열의 크기
	d3dResourceDesc.MipLevels = 1; // MIPMAP 수준
	d3dResourceDesc.Format = DXGI_FORMAT_UNKNOWN; // 형식
	d3dResourceDesc.Layout = D3D12_TEXTURE_LAYOUT_ROW_MAJOR;
	d3dResourceDesc.Flags = D3D12_RESOURCE_FLAG_NONE;
	d3dResourceDesc.SampleDesc.Count = 1; // 텍스처의 표본의 수
	d3dResourceDesc.SampleDesc.Quality = 0; // 텍스처의 표본의 수의 품질

	pd3dDevice->CreateCommittedResource(&d3dHeapProperties, D3D12_HEAP_FLAG_NONE, &d3dResourceDesc,
		D3D12_RESOURCE_STATE_VERTEX_AND_CONSTANT_BUFFER, NULL, __uuidof(m_pd3dcbWorldMatrix),
		(void**)&m_pd3dcbWorldMatrix);

	m_pd3dcbWorldMatrix->Map(0, NULL, (void**)&m_pcbMappedWorldMatrix);
}

